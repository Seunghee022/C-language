#include <stdio.h>

int main()
{
    double a,b;
    printf("실수를 입력하시오:");
    scanf("%lf %lf",&a,&b);   //double형의 실수를 입력받는 %lf

    printf("%lf %lf %lf %lf\n",a+b,a-b,a*b,a/b); //합,차,곱,몫 출력하기
}