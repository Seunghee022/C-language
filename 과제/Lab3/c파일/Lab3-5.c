#include <stdio.h>

int main()
{
    double x,y;
    printf("x좌표를 입력하시오:");
    scanf("%lf",&x);
    printf("y좌표를 입력하시오.:");
    scanf("%lf",&y);
    (x>0)?((y>0)?printf("1사분면\n"):printf("4사분면\n")):(y>0)?printf("2사분면\n"):printf("3사분면\n");
    // x>0가 참이면 y비교해서 1 or 4 사분면 출력, x<0이면 y비교해서 2 or 3 사분면 출력
}