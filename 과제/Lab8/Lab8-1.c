#include <stdio.h>
int main()
{
    int a[5]={1,2,3,4,5};
    int *p=a;  //a를 가르키는 포인터 변수
    for(int i=0;i<5;i++)
        printf("%d ",*(p+i));  //i를 더해서 인덱스이동
    printf("\n");
    return 0;
}