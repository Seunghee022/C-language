#include <stdio.h>
int main()
{
    int n;
    printf("정수를 입력하시오: ");
    scanf("%d",&n); 
    do
    {
        printf("%d",n%10);   //맨끝자리수 출력
        n=n/10;        //10으로 나눠서 출력한수 없애기
    } while (n!=0);
    printf("\n");
}