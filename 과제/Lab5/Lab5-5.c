#include <stdio.h>
int main()
{
    int a,b,c,n;
    a=0,b=1;
    printf("몇번째 항까지 구할까요? ");
    scanf("%d",&n);
    printf("%d %d ",a,b);
    for (int i=2;i<n;i++) //n번반복문
    {
        c=a+b;
        a=b;
        b=c;
        printf("%d ",c);
    }
    printf("\n");
}