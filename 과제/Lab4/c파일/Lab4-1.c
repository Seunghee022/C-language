#include <stdio.h>

int main()
{
    int a,b;
    printf("정수를 입력하시오:");
    scanf("%d",&a);
    printf("정수를 입력하시오:");
    scanf("%d",&b);
    if (a%b==0)     //a를 b로 나눈 나머지가 0이면 b는a의 약수
        printf("약수입니다.\n"); 
    else
        printf("약수가 아닙니다\n");
}