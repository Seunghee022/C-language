#include <stdio.h>
int main()
{
    int data;
    printf("16진수 정수를 입력하시오: ");
    scanf("%x",&data);
    printf("8진수로는 %#o입니다.\n",data); 
     //8진수(앞에0포함) 출력하기위해 %#o사용  
    printf("10진수로는 %d입니다.\n",data);
    printf("16진수로는 %#x입니다.\n",data);  
    //16진수(앞에 0x포함) 출력하기위해 %#x 사용
}