#include <stdio.h>
int main()
{
    double h;
    double w;
    double d;

    printf("상자의 가로 세로 높이를 한번에 입력: ");
    scanf("%lf %lf %lf",&w,&h,&d); // double 입력받을땐 %lf
    printf("상자의 부피는 %lf입니다.\n",h*w*d);
}